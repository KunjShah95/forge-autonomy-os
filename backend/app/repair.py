def generate_dependency_fix(service: str, log_output: str) -> RepairSuggestion:
    """Generate a dependency mismatch fix."""
    log_lower = log_output.lower()

    # Detect package manager
    if "npm" in log_lower or "node" in log_lower or "package.json" in log_lower:
        pm = "npm"
    elif "pip" in log_lower or "python" in log_lower or "requirements" in log_lower:
        pm = "pip"
    elif "go:" in log_lower or "go.mod" in log_lower:
        pm = "go"
    else:
        pm = "npm"  # default

    # Extract missing package name from log
    import re
    pkg_match = re.search(
        r"(?:cannot find|not found|No module|Module not found)[:\s]+([\w\-.@/]+)",
        log_output,
        re.IGNORECASE,
    )
    pkg_name = pkg_match.group(1) if pkg_match else "unknown-package"

    if pm == "npm":
        patch = f'''# Auto-fix: Add missing dependency {pkg_name}
- // {pkg_name} is missing from package.json
+ "{pkg_name}": "^1.0.0",
# Additionally, make the import resilient:
- const mod = require('{pkg_name}')
+ const mod = require('{pkg_name}') ?? null
+ if (!mod) throw new Error('{pkg_name} is required but could not be loaded')'''
    elif pm == "pip":
        patch = f'''# Auto-fix: Add missing dependency {pkg_name}
- # {pkg_name} is missing
+ {pkg_name}>=1.0.0'''
    elif pm == "go":
        patch = f'''// Auto-fix: Add missing dependency
- // import path missing: {pkg_name}
+ import ("{pkg_name}")'''
    else:
        patch = f"# Auto-fix: Resolve missing dependency {pkg_name}"

    pr_title = f"fix({service}): resolve missing dependency {pkg_name}"
    pr_body = f"""## 🤖 Auto-generated Fix (Forge Autonomy OS)

**Classification**: dependency  
**Trace ID**: auto-generated  
**Service**: {service}  

### Root Cause
Missing dependency `{pkg_name}` detected in CI output.

### Proposed Fix
Add the missing dependency to the package manifest and add a defensive check to ensure graceful failure if the dependency can't be loaded.

### Evidence
The following error was detected in CI logs:
```
{log_output[:500]}
```

### Verification Steps
1. Apply the patch
2. Run `npm install` (or equivalent)
3. Re-run CI pipeline
4. Monitor for resolution

---

*This fix was automatically generated by Forge Autonomy OS.*
"""

    return RepairSuggestion(
        trace_id="auto-generated",
        fix_type="dependency",
        patch=patch,
        description=f"Resolve missing dependency {pkg_name} in {service} using {pm}",
        confidence=0.88,
        pr_title=pr_title,
        pr_body=pr_body,
    )


def generate_config_fix(service: str, log_output: str) -> RepairSuggestion:
    """Generate a config/fix for a TypeError or config issue."""
    patch = '''# Auto-fix: Add defensive null check
- const val = config.some.setting
+ const val = config?.some?.setting ?? defaultSetting
+ if (!config?.some) logger.warn('config.some is undefined, using default')'''

    return RepairSuggestion(
        trace_id="auto-generated",
        fix_type="config",
        patch=patch,
        description=f"Add defensive null checks and defaults for config access in {service}",
        confidence=0.85,
        pr_title=f"fix({service}): defensive config access pattern",
        pr_body=f"""## 🤖 Auto-generated Fix (Forge Autonomy OS)

**Classification**: config  
**Service**: {service}  

### Root Cause
Config property access error detected in CI logs — likely accessing nested properties without null checks.

### Proposed Fix
Add optional chaining and default fallbacks to prevent runtime type errors.

---

*This fix was automatically generated by Forge Autonomy OS.*
""",
    )


def generate_flake_fix(service: str, log_output: str) -> RepairSuggestion:
    """Generate a fix for flaky test — add retry logic."""
    patch = '''# Auto-fix: Add retry logic for flaky test
- expect(result).toBe(expected)
+ await expect.poll(async () => result, { timeout: 10000, interval: 500 }).toBe(expected)'''

    return RepairSuggestion(
        trace_id="auto-generated",
        fix_type="flake",
        patch=patch,
        description=f"Add retry/poll logic to flaky test assertions in {service}",
        confidence=0.75,
        pr_title=f"test({service}): stabilize flaky test with retry assertion",
        pr_body=f"""## 🤖 Auto-generated Fix (Forge Autonomy OS)

**Classification**: flake  
**Service**: {service}  

### Root Cause
Flaky test detected — likely a timing-dependent assertion.

### Proposed Fix
Replace direct assertion with a polling assertion that retries until the expected value is available.

---

*This fix was automatically generated by Forge Autonomy OS.*
""",
    )


# ---------------------------------------------------------------------------
# Routes
# ---------------------------------------------------------------------------

@router.post("/repair", response_model=RepairSuggestion)
def generate_repair(req: RepairRequest):
    """Generate a repair suggestion for a CI failure classification."""
    if req.classification == "dependency":
        return generate_dependency_fix(req.service, req.log_output)
    elif req.classification == "config":
        return generate_config_fix(req.service, req.log_output)
    elif req.classification == "flake":
        return generate_flake_fix(req.service, req.log_output)
    else:
        return RepairSuggestion(
            trace_id=req.trace_id,
            fix_type="unclassified",
            patch="",
            description="Unable to auto-generate fix for unclassified failure. Manual investigation required.",
            confidence=0.1,
            pr_title="",
            pr_body="",
        )
